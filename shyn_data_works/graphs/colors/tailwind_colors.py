def get_tailwind_colors(version: Literal['v2', 'custom'], name:str, orient:Literal['dict', 'list'] = 'dict'):
    versions = {
        'v2': tailwind_colors_v2,
        'custom': tailwind_colors_custom
    }
    return versions[version](name, orient)

